N=15;
n=0:N-1;    %проходимо по значеннях від 0 до N-1
x=double(n==0); %перевірка, чи дорівнює n нулю
y=double(n==5);
figure(1)   %відкриваємо фігуру
subplot(4,1,1),stem(x)  %subplot()-поділяє фігуру на частини
subplot(4,1,2),stem(x-2)    %stem()-будує графік за допомогою веритикальних ліній
subplot(4,1,3),stem(y)
subplot(4,1,4),stem(y-2)